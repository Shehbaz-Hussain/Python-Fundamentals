"""
File: solution03.py

Solution to Exercise 03: Convert an Integer to a String

This program demonstrates how to convert an integer into a string
using the str() function.
"""

# Store the integer value in a variable
student_id = 105

# Display the original value
print("Original Value:", student_id)

# Display the original data type
print("Original Type:", type(student_id))

print()

# Convert the integer to a string
student_id_text = str(student_id)

# Display the converted value
print("Converted Value:", student_id_text)

# Display the converted data type
print("Converted Type:", type(student_id_text))