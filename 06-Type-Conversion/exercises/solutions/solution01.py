"""
File: solution01.py

Solution to Exercise 01: Convert a String to an Integer

This program demonstrates how to convert a string into an integer
using the int() function.
"""

# Store the string value in a variable
number_text = "25"

# Display the original value
print("Original Value:", number_text)

# Display the original data type
print("Original Type:", type(number_text))

print()

# Convert the string to an integer
number = int(number_text)

# Display the converted value
print("Converted Value:", number)

# Display the converted data type
print("Converted Type:", type(number))