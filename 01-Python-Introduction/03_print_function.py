"""
File: 03_print_function.py

Description:
This program demonstrates the Python print() function.
"""

# Printing simple text
print("Welcome to Python!")

# Printing numbers
print(100)
print(25.75)

# Printing multiple values
print("Age:", 20)

# Using separators
print("Python", "AI", "Programming", sep=" | ")

# Ending without a new line
print("Hello", end=" ")
print("World!")

# Printing mathematical results
print("10 + 5 =", 10 + 5)

# Printing Boolean values
print(True)
print(False)