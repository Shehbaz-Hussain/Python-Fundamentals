# Example 01: AND Operator
# This program demonstrates how the 'and' logical operator works.

# Example 1
age = 25

print("Example 1")
print("Age:", age)
print("Age is between 18 and 60:", age >= 18 and age <= 60)

print()

# Example 2
marks = 75

print("Example 2")
print("Marks:", marks)
print("Marks are between 50 and 100:", marks >= 50 and marks <= 100)

print()

# Example 3
number = 8

print("Example 3")
print("Number:", number)
print("Number is positive and even:", number > 0 and number % 2 == 0)

print()

# Example 4
temperature = 35

print("Example 4")
print("Temperature:", temperature)
print("Temperature is between 20 and 30:", temperature >= 20 and temperature <= 30)


# Expected Output:

# Example 1
# Age: 25           
# Age is between 18 and 60: True

# Example 2
# Marks: 75
# Marks are between 50 and 100: True

# Example 3
# Number: 8
# Number is positive and even: True

# Example 4
# Temperature: 35
# Temperature is between 20 and 30: False   