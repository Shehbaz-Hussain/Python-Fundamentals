# Example 18: Classifying a Number
# ============================================================
# Description:
# This program demonstrates how multiple conditional
# statements can be used to classify a number as positive,
# negative, or zero.
# ============================================================

# Prompt the user to enter a number
number = int(input("Enter a number: "))

# Classify the entered number
if number > 0:
    print("The number is positive.")
elif number < 0:
    print("The number is negative.")
else:
    print("The number is zero.")

# This statement always executes
print("Program execution completed.")

