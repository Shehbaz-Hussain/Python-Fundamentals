# ============================================================
# Example 04: Checking Eligibility Based on Age
# ============================================================
# Description:
# This program demonstrates how an if statement can be used
# to make a simple decision based on user input. If the user's
# age is 18 or above, the program displays an eligibility
# message.
# ============================================================

# Prompt the user to enter their age
age = int(input("Enter your age: "))

# Check whether the user is eligible
if age >= 18:
    print("You are eligible to vote.")

# This statement always executes
print("Program execution completed.")

