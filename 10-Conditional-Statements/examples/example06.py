# ============================================================
# Example 06: Finding the Larger of Two Numbers
# ============================================================
# Description:
# This program demonstrates how an if-else statement can be
# used to compare two values. The program displays the larger
# of the two entered numbers.
# ============================================================

# Prompt the user to enter two numbers
first_number = int(input("Enter the first number: "))
second_number = int(input("Enter the second number: "))

# Compare the two numbers
if first_number > second_number:
    print("The larger number is:", first_number)
else:
    print("The larger number is:", second_number)

# This statement always executes
print("Program execution completed.")

