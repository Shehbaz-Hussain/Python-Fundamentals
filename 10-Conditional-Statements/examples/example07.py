# ============================================================
# Example 07: Checking Pass or Fail
# ============================================================
# Description:
# This program demonstrates how an if-else statement can be
# used to make a decision based on a minimum passing score.
# A student scoring 50 or more marks is considered to have
# passed the examination.
# ============================================================

# Prompt the user to enter examination marks
marks = int(input("Enter your marks: "))

# Check the result
if marks >= 50:
    print("Result: Pass")
else:
    print("Result: Fail")

# This statement always executes
print("Program execution completed.")

