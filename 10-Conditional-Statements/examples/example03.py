# ============================================================
# Example 03: Checking Whether a Number is Zero
# ============================================================
# Description:
# This program demonstrates how an if statement can be used
# to test for an exact value. If the entered number is equal
# to zero, a message is displayed.
# ============================================================

# Prompt the user to enter a number
number = int(input("Enter a number: "))

# Check whether the number is equal to zero
if number == 0:
    print("The number is zero.")

# This statement always executes
print("Program execution completed.")

