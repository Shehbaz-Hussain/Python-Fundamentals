# Numeric types example

age = 20
height = 1.75
complex_number = 3 + 4j

print("Age:", age)
print("Height:", height)
print("Complex number:", complex_number)

# Expected output:
# Age: 20
# Height: 1.75
# Complex number: (3+4j)

# Explanation:
# This program demonstrates int, float, and complex values.

# Time complexity: O(1)
# Memory notes: All values are small objects stored in memory.
