# Collection examples

fruits = ["apple", "banana", "cherry"]
point = (10, 20)
numbers = {1, 2, 2, 3}
student = {"name": "Mina", "age": 21}

print(fruits)
print(point)
print(numbers)
print(student)

# Expected output:
# ['apple', 'banana', 'cherry']
# (10, 20)
# {1, 2, 3}
# {'name': 'Mina', 'age': 21}

# Explanation:
# This program demonstrates list, tuple, set, and dict.

# Time complexity: O(1) average for membership and lookup operations.
# Memory notes: Collections store references to individual elements.
