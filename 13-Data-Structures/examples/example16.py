"""
Module 13 - Data Structures
Example 16: Iterating Over a Set

Purpose:
Demonstrate how to iterate through the elements of a set using
a for loop.
"""

# Create a set of programming languages.
programming_languages = {
    "Python",
    "Java",
    "C++",
    "JavaScript"
}

print("Programming Languages:")

# Iterate through each element in the set.
for language in programming_languages:
    print(language)

# Expected Output:
# Programming Languages:
# Python
# Java
# C++
# JavaScript
#
# Note:
# The order of elements in a set is not guaranteed.