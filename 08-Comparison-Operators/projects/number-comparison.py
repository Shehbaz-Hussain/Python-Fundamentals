# Number Comparison

number1 = float(input("Enter the first number: "))
number2 = float(input("Enter the second number: "))

print("First number is greater:", number1 > number2)
print("First number is less:", number1 < number2)
print("Both numbers are equal:", number1 == number2)
print("Numbers are not equal:", number1 != number2)
print("First number is greater than or equal to the second:", number1 >= number2)
print("First number is less than or equal to the second:", number1 <= number2)