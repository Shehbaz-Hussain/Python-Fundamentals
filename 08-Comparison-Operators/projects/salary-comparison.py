# Salary Comparison

employee1_salary = float(input("Enter the salary of Employee 1: "))
employee2_salary = float(input("Enter the salary of Employee 2: "))

print("Employee 1 has a higher salary:", employee1_salary > employee2_salary)
print("Employee 2 has a higher salary:", employee1_salary < employee2_salary)
print("Both employees have the same salary:", employee1_salary == employee2_salary)