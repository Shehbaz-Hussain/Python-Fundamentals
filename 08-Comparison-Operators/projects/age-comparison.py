# Age Comparison

person1_age = int(input("Enter the age of the first person: "))
person2_age = int(input("Enter the age of the second person: "))

print("First person is older:", person1_age > person2_age)
print("Second person is older:", person1_age < person2_age)
print("Both have the same age:", person1_age == person2_age)