# Marks Comparison

student1_marks = int(input("Enter marks of Student 1: "))
student2_marks = int(input("Enter marks of Student 2: "))

print("Student 1 has higher marks:", student1_marks > student2_marks)
print("Student 2 has higher marks:", student1_marks < student2_marks)
print("Both students have equal marks:", student1_marks == student2_marks)