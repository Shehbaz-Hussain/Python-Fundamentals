"""Example 12: A simple calculator using input and output."""

# Read two numbers from the user
first_number = float(input("Enter first number: "))
second_number = float(input("Enter second number: "))

# Calculate the sum
sum_result = first_number + second_number

# Display the result
print(f"The sum is: {sum_result}")

# Sample run:
# Enter first number: 3.5
# Enter second number: 2.5
# The sum is: 6.0

# Explanation:
# The program reads two numbers from the user, converts them to
# floating-point values, adds them together, and displays the sum.

