"""Example 7: Using the format() method."""

name = "Nadia"
city = "Lahore"

# Use the format() method to insert values into strings
print("Hello, {}!".format(name))
print("You live in {}".format(city))

# Expected output:
# Hello, Nadia!
# You live in Lahore

# Explanation:
# The format() method is another way to build readable strings.

