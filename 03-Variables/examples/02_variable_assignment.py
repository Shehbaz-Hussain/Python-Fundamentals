"""Example 2: Variable assignment.

Purpose:
    Show how values are assigned to variables.

Description:
    This example demonstrates the assignment operator and how variables store values.
"""

# Assign values to variables
city = "London"
population = 9000000

# Print the values
print(city)
print(population)

# Expected output:
# London
# 9000000

# Beginner explanation:
# The '=' symbol assigns the value on the right to the variable on the left.
# Here, city stores the text "London" and population stores the number 9000000.
