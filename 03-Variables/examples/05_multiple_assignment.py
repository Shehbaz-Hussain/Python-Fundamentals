"""Example 5: Multiple assignment.

Purpose:
    Demonstrate assigning multiple values in one line.

Description:
    This example shows how to assign values to several variables at once.
"""

# Assign multiple values in one line
x, y, z = 10, 20, 30

# Print the values
print(x)
print(y)
print(z)

# Expected output:
# 10
# 20
# 30

# Beginner explanation:
# Python allows multiple assignment in a single statement.
# Each value is assigned to the matching variable in order.
