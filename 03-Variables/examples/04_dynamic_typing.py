"""Example 4: Dynamic typing.

Purpose:
    Show that Python variables can change type.

Description:
    In Python, a variable can hold an integer at one point and a string later.
"""

# Assign an integer
value = 10
print(value)

# Reassign a string
value = "ten"
print(value)

# Expected output:
# 10
# ten

# Beginner explanation:
# Python is dynamically typed, which means the same variable can store different types.
# This is different from some other languages that require a fixed type.
