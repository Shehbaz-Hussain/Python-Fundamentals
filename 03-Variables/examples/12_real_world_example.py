"""Example 12: Real-world example with variables.

Purpose:
    Show how variables can represent real-world data.

Description:
    This example models a simple product purchase using variables.
"""

# Store real-world values in variables
product_name = "Laptop"
price = 800
quantity = 2

total_cost = price * quantity

# Print the result
print(product_name)
print(total_cost)

# Expected output:
# Laptop
# 1600

# Beginner explanation:
# Variables can represent real-world data such as product names and prices.
# They help make programs easier to understand and maintain.
