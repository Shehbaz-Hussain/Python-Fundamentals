"""Example 7: Variable naming rules.

Purpose:
    Demonstrate valid and invalid variable naming patterns.

Description:
    This example shows how Python variable names must follow specific rules.
"""

# Valid names
user_name = "Ava"
age2 = 20

# Print them
print(user_name)
print(age2)

# Expected output:
# Ava
# 20

# Beginner explanation:
# Variable names can contain letters, digits, and underscores.
# They should not start with a digit and should not contain spaces.
# Using descriptive names such as user_name is recommended.
