"""Example 1: Basic variables in Python.

Purpose:
    Demonstrate how to create variables and print their values.

Description:
    This example shows how variables store simple values such as strings and numbers.
"""

# Create variables
name = "Alice"
age = 25

# Print the values
print(name)
print(age)

# Expected output:
# Alice
# 25

# Beginner explanation:
# A variable is a named place in memory that stores data.
# Here, name stores the text "Alice" and age stores the number 25.
