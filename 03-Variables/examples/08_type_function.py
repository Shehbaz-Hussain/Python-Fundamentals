"""Example 8: Using the type() function.

Purpose:
    Show how to check the type of a variable.

Description:
    This example uses Python's built-in type() function to inspect a variable's data type.
"""

# Create variables
age = 21
name = "Mina"

# Print their types
print(type(age))
print(type(name))

# Expected output:
# <class 'int'>
# <class 'str'>

# Beginner explanation:
# The type() function helps you discover what kind of value a variable holds.
# In this example, age is an integer and name is a string.
