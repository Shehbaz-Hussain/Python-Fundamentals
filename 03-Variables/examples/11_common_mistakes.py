"""Example 11: Common beginner mistakes with variables.

Purpose:
    Highlight common errors when working with variables.

Description:
    This example shows examples of invalid names and incorrect assignment syntax.
"""

# Valid variable names
student_name = "Nora"
student_age = 19

# Print them
print(student_name)
print(student_age)

# Expected output:
# Nora
# 19

# Beginner explanation:
# Avoid using invalid names such as starting with a digit or using spaces.
# Also remember that '=' assigns a value, while '==' compares values.
