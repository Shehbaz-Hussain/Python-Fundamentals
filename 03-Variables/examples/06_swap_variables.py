"""Example 6: Swapping variables.

Purpose:
    Demonstrate how to swap two variable values.

Description:
    This example uses tuple unpacking to exchange values between variables.
"""

# Create two variables
first = 10
second = 20

# Swap the values
first, second = second, first

# Print the swapped values
print(first)
print(second)

# Expected output:
# 20
# 10

# Beginner explanation:
# Python makes swapping easy with a single assignment statement.
# The values of first and second are exchanged.
