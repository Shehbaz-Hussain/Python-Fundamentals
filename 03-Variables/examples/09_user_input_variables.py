"""Example 9: Storing user input in variables.

Purpose:
    Demonstrate how input values can be stored in variables.

Description:
    This example uses input() to capture values from the user and store them.
"""

# Ask the user for input
name = input("Enter your name: ")
age = input("Enter your age: ")

# Print the values
print("Name:", name)
print("Age:", age)

# Expected output:
# Enter your name: Alice
# Enter your age: 25
# Name: Alice
# Age: 25

# Beginner explanation:
# input() reads a value from the user and stores it in a variable.
# The values are then printed for display.
