"""Example 10: Using variables in expressions.

Purpose:
    Show how variables can be used in arithmetic expressions.

Description:
    This example computes a result using values stored in variables.
"""

# Create variables
base = 10
height = 5

# Use variables in an expression
area = base * height

# Print the result
print(area)

# Expected output:
# 50

# Beginner explanation:
# Variables can be used in calculations.
# Here, base and height are multiplied to compute the area.
