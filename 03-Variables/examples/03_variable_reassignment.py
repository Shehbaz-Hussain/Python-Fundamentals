"""Example 3: Variable reassignment.

Purpose:
    Demonstrate that variable values can be changed.

Description:
    This example shows how to assign a new value to an existing variable.
"""

# Create a variable
score = 10

# Reassign the variable
score = 15

# Print the updated value
print(score)

# Expected output:
# 15

# Beginner explanation:
# Variables are not fixed forever; they can be reassigned to new values.
# Here, score changes from 10 to 15.
